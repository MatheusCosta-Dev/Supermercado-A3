package mercado;
public abstract class Pessoa{
    protected String nome, CPF;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }
   
    public abstract void consultarDados();

}
